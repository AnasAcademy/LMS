<div>
    <h2 class="mt-20 mb-20">متطلبات القبول في برنامج الماجستير المهني</h2>
    <ol type="1" class="ml-15">
        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">إرفاق شهادة البكالوريوس:</span>
            يتطلب على المتقدم تقديم شهادة البكالوريوس أو ما يعادلها من مؤهلات دراسية.
        </li>


        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">إرفاق نسخة من السجل الأكاديمي: </span>
            يتطلب على المتقدم تقديمكشف الدرجات الخاص بشهادة البكالوريوس.
        </li>

        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">إرفاق نسخة من بطاقة الهوية الوطنية أو جواز السفر:</span>
            تطلب على المتقدم تقديم نسخة من جواز السفر أو بطاقة الهوية الوطنية ساري المفعول.
        </li>

        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">سيرة ذاتية حديثة للمتقدم: </span>
            يتطلب على المتقدم تقديم سيرة ذاتيه حديثه.
        </li>

        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">الغرض من الدراسة: </span>
            يتطلب على المتقدم كتابة ورقة وما يعرف بـ (Statement of Purpose) يشرح أسباب رغبته في دراسة التخصص.

        </li>


        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">الخبرة العملية في نفس التخصص: </span>
            يتطلب من المتقدم خطاب فعلي لخبرة عملية خمس سنوات كحد أدنى في مجالات الفنون البصرية.
        </li>

        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">توصية علمية ومهنية: </span>
            يتطلب على المتقدم بتقديم توصيتين من أساتذة سبق لهم تدريسه في المراحل الدراسية ما بعد التعليم العام وإذا تعذر ذلك، فتكون من المديرين/ المشرفين الذين عمل معهم المتقدم خلال السنوات الخمس الماضية.
        </li>

        <li style="list-style: inherit" class="mb-15">
            <span class="font-weight-bold">الخلفية المهنية: </span>
            قد يُطلب من المتقدمين الذين خلفيتهم المهنية ليست في مجالات الفنون البصرية دراسة برنامج تأسيسي أو ما يعرف بـ (Foundation Courses).
            </li>
    </ol>
</div>
